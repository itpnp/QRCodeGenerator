    private void showData(List<MasterProduct> list){
        tableHeaders = new Vector<>();
        tableData = new Vector();
        tableHeaders.add("Kode Barang");
        tableHeaders.add("Nama Barang");
        tableHeaders.add("Jumlah");
        tableHeaders.add("Tipe");
        for(Object obj : list){
            MasterProduct masterProduct = (MasterProduct) obj;
            Vector<Object> row = new Vector<>();
            row.add(masterProduct.getKodeBarang());
            row.add(masterProduct.getNamaBarang());
            row.add(masterProduct.getJumlah());
            row.add(masterProduct.getType());
            tableData.add(row);
        }
        viewTable.setModel(new DefaultTableModel(tableData, tableHeaders));
        
    }

